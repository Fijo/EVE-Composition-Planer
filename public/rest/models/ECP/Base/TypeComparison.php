     * If the primary key is not null, return the hashcode of the
     * primary key. Otherwise, return the hash code of the object.
     *
     * @return int Hashcode
     */
    public function hashCode()
    {
        $validPk = null !== $this->getId() &&
            null !== $this->getTypeid() &&
            null !== $this->getComparisonid();

        $validPrimaryKeyFKs = 2;
        $primaryKeyFKs = [];

        //relation typecomparison_fk_af1a2f to table type
        if ($this->aType && $hash = spl_object_hash($this->aType)) {
            $primaryKeyFKs[] = $hash;
        } else {
            $validPrimaryKeyFKs = false;
        }

        //relation typecomparison_fk_0338d3 to table comparison
        if ($this->aComparison && $hash = spl_object_hash($this->aComparison)) {
            $primaryKeyFKs[] = $hash;
        } else {
            $validPrimaryKeyFKs = false;
        }

        if ($validPk) {
            return crc32(json_encode($this->getPrimaryKey(), JSON_UNESCAPED_UNICODE));
        } elseif ($validPrimaryKeyFKs) {
            return crc32(json_encode($primaryKeyFKs, JSON_UNESCAPED_UNICODE));
        }

        return spl_object_hash($this);
    }
        
